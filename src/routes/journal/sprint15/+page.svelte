	<h3>Argumentatie</h3>
	<h3>Vier Stappen voor het Opstellen van een Argumentatie</h3>
	<ul>
		<li>Formuleer een standpunt en selecteer argumenten</li>
		<li><strong>P1:</strong> Als je onderzoek doet, kun je een standpunt innemen.</li>
		<li><strong>P2:</strong> Jij verricht onderzoek naar een tech stack.</li>
		<li><strong>C:</strong> Dus jij kunt een standpunt innemen.</li>
	</ul>
	<strong><q>P = propositie, C = conclusie</q></strong>
	<p>
		Bedenk zowel argumenten voor als tegen jouw gekozen tech stack. Houd rekening met de vraag van
		de opdrachtgever, de behoeften van de gebruiker, de Developer Experience, enzovoort.
	</p>
	<h3>Argumentatieschema</h3>
	<ul>
		<li>
			<strong>Enkelvoudige argumentatie:</strong> EÃ©n argument ter ondersteuning van het standpunt.
		</li>
		<li>
			<strong>Meervoudige argumentatie:</strong> Meerdere, afzonderlijke argumenten ter ondersteuning
			van het standpunt.
		</li>
		<li>
			<strong>Nevenschikkende argumentatie:</strong> Meerdere argumenten die gezamenlijk het standpunt
			ondersteunen.
		</li>
		<li>
			<strong>Onderschikkende argumentatie:</strong> Een argument dat zelf weer ondersteund wordt door
			een ander argument.
		</li>
	</ul>
	<h3>Beoordeling van de Aanvaardbaarheid van de Argumentatie</h3>
	<ul>
		<li>Is de argumentatie voldoende uitgewerkt?</li>
		<li>Is de argumentatie inhoudelijk correct?</li>
		<li>Ondersteunt de argumentatie het ingenomen standpunt?</li>
	</ul>
	<h3>Uitwerking van het Argumentatieschema</h3>
	<p>
		Het gekozen standpunt wordt ondersteund door een combinatie van enkelvoudige en onderschikkende
		argumentatie. De kern van het standpunt is dat een tech stack zorgvuldig moet worden gekozen op
		basis van onderzoeksresultaten. Onderzoek geeft namelijk inzicht in belangrijke eigenschappen
		zoals prestaties en schaalbaarheid. Dit standpunt wordt verder ondersteund door een specifiek
		voorbeeld: de keuze voor een tech stack zoals Node.js vanwege bewezen efficiÃ«ntie in realtime
		toepassingen. Dit voorbeeld versterkt de betrouwbaarheid van het argument.
	</p>
	<p>
		Conclusie: De argumentatie is inhoudelijk correct en voldoende uitgewerkt. Het ingenomen
		standpunt wordt hierdoor overtuigend onderbouwd en is daarmee aanvaardbaar.
	</p>
