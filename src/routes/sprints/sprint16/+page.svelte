    <h3>Morfologische Kaart</h3>
    <p>Een morfologische kaart is een ontwerp tool waarmee je systematisch verschillende oplossingen voor een complex probleem kunt verkennen. Het proces werkt als volgt:</p>
    <ul>
        <li><strong>Definieer functies:</strong> Bepaal de belangrijkste functies of aspecten van het ontwerp.</li>
        <li><strong>Bedenk varianten:</strong> Voor elke functie maak je een lijst met mogelijke oplossingen.</li>
        <li><strong>Vul de matrix:</strong> Plaats de functies als kolommen en de varianten als rijen in een tabel.</li>
        <li><strong>Combineer en evalueer:</strong> Door verschillende varianten te combineren, ontstaan ontwerpopties die je vervolgens beoordeelt op basis van criteria als kosten en haalbaarheid.</li>
    </ul>
