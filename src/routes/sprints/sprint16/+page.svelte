    <h3>Component Library</h3>
    <p>
        Een component library is een verzameling van vooraf gemaakte, geteste, en goed gedocumenteerde
        UI-componenten die herbruikbaar zijn in de gebruikersinterface van een product. Dit zorgt
        ervoor dat het product een consistente uitstraling en gebruikerservaring heeft, en bevordert
        daarnaast effici√´ntie en schaalbaarheid. Het gebruik van component libraries stelt ontwerpers
        en ontwikkelaars in staat om snel nieuwe functies en pagina's toe te voegen, terwijl de
        algehele ontwerp consistentie en kwaliteit behouden blijft.
    </p>
    <h3>RAP/PE-Websites</h3>
    <p>De websites moeten voldoen aan de volgende richtlijnen:</p>
    <ul>
        <li><strong>Responsive:</strong> Geschikt voor verschillende schermformaten.</li>
